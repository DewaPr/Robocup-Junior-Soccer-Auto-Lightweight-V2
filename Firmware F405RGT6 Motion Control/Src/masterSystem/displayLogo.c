unsigned long logoSlideTime = 0;
int logoSlideOffset = 127;
int logoSlideOffChange = 0;
uint8_t logoSlideAnimation = 0;
void displayLogo(){
  logoSlideOffChange = 0;
  while(1){
    if(HAL_GetTick() - logoSlideTime > ANIMATION_UPDATE_MILLIS){
      logoSlideTime = HAL_GetTick();

      drawLogo(logoSlideOffset);
      if(logoSlideOffset > logoSlideOffChange){
        if(logoSlideAnimation == 0) logoSlideOffset -= ceil(abs(logoSlideOffset - logoSlideOffChange) * ANIMATION_SLIDE_SPEED);
        else {
          logoSlideOffset -= ceil((abs(logoSlideOffset) + 2) * ANIMATION_SLIDE_SPEED);
          // logoSlideOffset -= 2;
          if(logoSlideOffset <= logoSlideOffChange) break;
        }
      }
      if(ceil(logoSlideOffset == 0) && logoSlideAnimation == 0){
        HAL_Delay(500);
        logoSlideOffChange = -127;
        logoSlideAnimation = 1;
      }
    }
  }
}
